#include<stdio.h>
int main()
{
 int num1,num2;
 printf("please enter first number ");
 scanf("%d",&num1);
 printf("please enter second number ");
 scanf("%d",&num2);
 printf("%d+%d=%d\n",num1,num2,num1+num2);
 printf("%d-%d=%d\n",num1,num2,num1-num2);
 printf("%d*%d=%d\n",num1,num2,num1*num2);
 printf("%d/%d=%d\n",num1,num2,num1/num2);
 return 0;
}
